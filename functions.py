def add_numbers(alpha: int, beta: int) -> int:
    """adds two numbers

    Args:
        alpha (int): first number
        beta (int): second number

    Returns:
        int: sum
    """
    return alpha + beta


def multiply_numbers(alpha: int, beta: int) -> int:
    """Multiply two numbers

    Args:
        alpha (int): first number
        beta (int): second number

    Returns:
        int: result of multiplication
    """
    return alpha * beta
